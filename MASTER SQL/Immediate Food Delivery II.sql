/*
Write an SQL query to find the percentage of immediate orders in the first orders of all 
customers, rounded to 2 decimal places.

Delivery table:
+-------------+-------------+------------+-----------------------------+
| delivery_id | customer_id | order_date | customer_pref_delivery_date |
+-------------+-------------+------------+-----------------------------+
| 1           | 1           | 2019-08-01 | 2019-08-02                  |
| 2           | 2           | 2019-08-02 | 2019-08-02                  |
| 3           | 1           | 2019-08-11 | 2019-08-12                  |
| 4           | 3           | 2019-08-24 | 2019-08-24                  |
| 5           | 3           | 2019-08-21 | 2019-08-22                  |
| 6           | 2           | 2019-08-11 | 2019-08-13                  |
| 7           | 4           | 2019-08-09 | 2019-08-09                  |
+-------------+-------------+------------+-----------------------------+

Result table:
+----------------------+
| immediate_percentage |
+----------------------+
| 50.00                |
+----------------------+
*/


WITH result as 
(
select *, rank() over(partition by customer_id order by order_date)  ranking 
from delivery 
)

select round(avg(case when order_date = customer_pref_delivery_date then 1.00 else 0.00 end)*100, 2)  immediate_percentage 
from result
where ranking = 1 



